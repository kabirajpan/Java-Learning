package day04;

public class Day04_resultingDataType {
    public static void main(String[] args) {

        // Resulting data types
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;
        System.out.println(b);

        // Increment and Decrement Data types

        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);


        int j = 5;
        int q = 3;
        System.out.println(++j *q);

    }
}
